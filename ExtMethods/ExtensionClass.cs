using System;
using System.Collections.Generic;
using System.Text;

namespace ExtMethods
{
    public class ExtensionClass
    {
        public int AddNumber (int Number1, int Number2)
        {
            return Number1 + Number2; 
        }

        public int SubNumber(int Number1 , int Number2)
        {
            return Number1 - Number2;
        }
    }
}
